package arrays;

public class Arrays {

	public static void main(String[] args) {
		/* This calendar program should output 
		 * all the days of the week.
		 */
		String[] days = {"Monday", "Tuesday", "Wednesday","Thursday",
		              "Friday", "Saturday", "Sunday"};
		for (int i = 0; i<7; i++) {
			System.out.println(days[i]);
		}
	}

}
